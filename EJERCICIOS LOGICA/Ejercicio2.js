alert ("Hola bienvenido");

let primerNumero = parseFloat (prompt ("Digite primer numero"));

let SegundoNumero = parseFloat (prompt ("Digite segundo numero"));

let producto = primerNumero * SegundoNumero

let modulo = primerNumero % SegundoNumero

let cociente = primerNumero / SegundoNumero

let suma = primerNumero + SegundoNumero

let resta = primerNumero - SegundoNumero

alert (`Los resultados de ${primerNumero} y ${SegundoNumero} son:

    produto ${producto} 
    modulo ${modulo} 
    cociente${cociente}
    suma ${suma}
    resta ${resta}`);



